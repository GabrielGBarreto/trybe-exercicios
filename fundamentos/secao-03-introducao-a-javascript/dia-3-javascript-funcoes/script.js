let saldo = 150

// ->Adicione um valor ao saldo.

function alterasaldo(index) {
    return saldo + index
}
// console.log(alterasaldo(30))



// ->Subtraia um valor do saldo.

function alterasaldo2(index2) {
    return saldo - index2
}
// console.log(alterasaldo2(50))



// ->Multiplique o valor do saldo por uma taxa.

function alterasaldo3(index3) {
    return saldo * index3
}
// console.log(alterasaldo3(3))



// ->Divida o valor do saldo.

function alterasaldo4(index4) {
    return saldo / index4
}
console.log(alterasaldo4(3))